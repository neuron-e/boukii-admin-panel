export type NavigationItem = NavigationLink | NavigationDropdown | NavigationSubheading;

export interface NavigationLink {
  type: 'link';
  route: string | any;
  fragment?: string;
  label: string;
  icon?: string;
  icon_active?: string;
  routerLinkActiveOptions?: { exact: boolean };
  permissions?: string[];
  roles?: string[];
  badge?: {
    value: string;
    bgClass: string;
    textClass: string;
  };
}

export interface NavigationDropdown {
  type: 'dropdown';
  label: string;
  icon?: string;
  permissions?: string[];
  roles?: string[];
  children: Array<NavigationLink | NavigationDropdown>;
  badge?: {
    value: string;
    bgClass: string;
    textClass: string;
  };
}

export interface NavigationSubheading {
  type: 'subheading';
  label: string;
  permissions?: string[];
  roles?: string[];
  children: Array<NavigationLink | NavigationDropdown>;
}
